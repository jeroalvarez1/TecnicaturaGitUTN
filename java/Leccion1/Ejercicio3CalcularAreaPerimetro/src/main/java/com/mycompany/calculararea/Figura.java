package com.mycompany.calculararea;

public class Figura {

    private float altura = 1f;
    private float base = 1f;
    
    public Figura() {
        super();
    }

    public void setAltura(float altura) {
        this.altura = altura;
    }

    public float getAltura() {
        return altura;
    }

    public void setBase(float base) {
        this.base = base;
    }

    public float getBase() {
        return base;
    }

}
